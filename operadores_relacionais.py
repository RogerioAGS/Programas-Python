# Operadores Relacionais
# O resultado da operação entre um operador relacional será verdadeiro
# True ou falso - False

n1 = 3
n2 = 2

# Igualdade ==
res1 = (n1 == n2)
print(res1)

# Diferente !=
res2 = (n1 != n2)
print(res2)

# Menor <
res3 = (n1 < n2)
print(res3)

# Menor ou igual <=
res4 = (n1 <= n2)
print(res4)

# Maior >
res5 = (n1 > n2)
print(res5)

# Maior ou igual >=
res6 = (n1 >= n2)
print(res6)
