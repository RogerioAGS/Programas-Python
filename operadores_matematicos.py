num1 = 2
num2 = 3

# Adição
soma = num1 + num2
print("Soma: ", soma)


# Subtração
sub = num1 - num2
print("Subtração: ", sub)

# Multiplicação
mult = num1 * num2
print("Multiplicação: " , mult)

# Divisão
div = num1 / num2
print("Divisão: ", div)

# Resto
resto = num2 % num1
print("Resto: ", resto )

# Exponte
exp = num1 ** num2
print("Exponenciação: ", exp)

# Expressões:combinação dos operadores
# 1º Parênteses ()
# 2º Multiplicação/Divisão
# 3º Soma/Subtração
