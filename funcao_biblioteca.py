# Funções de Operações
def somar(num1, num2):
    res = num1 + num2
    return res

def subtrair(num1, num2):
    res = num1 - num2
    return res

def multiplicar(num1, num2):
    res = num1 * num2
    return res

def dividir(num1, num2):
    res = num1 / num2
    return res

def pot(base, exp):
    res = base ** exp
    return res